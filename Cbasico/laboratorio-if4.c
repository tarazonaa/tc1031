/*Programa que crea una condicion 
if-else... if-else*/
#include <stdio.h>
int main () {
   int a = 100;
   if( a == 10 ) {
      printf("El valor es 10\n" );
   } else if( a == 20 ) {
      printf("El valor es 20\n" );
   } else {
      printf("No es 10 o 20\n" );
   }
   return 0;
}
